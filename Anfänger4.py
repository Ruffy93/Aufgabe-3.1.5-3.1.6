note = int(input("Gib eine Note von 1 bis 6 ein: "))

if note == 1:
    print("Sehr gut")
elif note == 2:
    print("Gut")
elif note == 3:
    print("Befriedigend")
elif note == 4:
    print("Ausreichend")
elif note == 5:
    print("Mangelhaft")
elif note == 6:
    print("Ungenügend")
else:
    print("Ungültige Eingabe")
